#include <iostream>
using namespace std;

int main()
{
	int A = 0;
	A = 100;


	cout << A + 3 << endl;
	cout << A - 3 << endl;
	cout << A * 3 << endl;
	cout << A / 3 << endl;
	cout << A % 3 << endl;
	cout << endl;


	float B = 0;
	B = 2.1f;

	cout << B + 0.3f << endl;
	cout << B - 0.3f << endl;
	cout << B * 0.3f << endl;
	cout << B / 0.3f << endl;



	return 0;
}
